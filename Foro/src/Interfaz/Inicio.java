    private void btnIngresarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnIngresarMouseClicked
        Tema tem = new Tema(txtNombreUsuario.getText());

        if(this.validar()){
            JOptionPane.showMessageDialog(null, "Bienvenido Usuario " + txtNombreUsuario.getText());
            tem.show();
            this.hide();
        }
    }//GEN-LAST:event_btnIngresarMouseClicked

    private boolean validar() {
        if(txtNombreUsuario.getText().equals("")){
            JOptionPane.showMessageDialog(null, "nombre de usuario es obligatorio");
            txtNombreUsuario.requestFocus();

            return false;
        }

        if(txtPassword.getText().equals("")){
            JOptionPane.showMessageDialog(null, "password es obligatorio");
            txtPassword.requestFocus();

            return false;
        }

        if(!usu.EsUsuario(txtNombreUsuario.getText(), txtPassword.getText())){
            JOptionPane.showMessageDialog(null, "nombre de usuario y/o password inv√°lido");
            txtNombreUsuario.requestFocus();
            
            return false;
        }

        return true;
    }

